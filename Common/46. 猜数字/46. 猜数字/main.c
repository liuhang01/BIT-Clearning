#include <stdio.h>
int main() {
    int a[6],i=0;
    int max=0,anss=1,ans=0;
    do{
        if(1!=scanf("%d",&a[i])){
            return 0;
        }
        if (max<a[i]) {
            max=a[i];
        }
    }while(a[i++]!=0);
    i--;
    for (int j=0; j<i; j++) {
        anss=1;
        for (int k=0; k<a[j]-1; k++) {
            anss = anss << 1;
        }
        ans+=anss;
    }
    printf("%d\n",ans);
    return 0;
}

